/*
 * eprintf()
 *
 * this is used by gcc for the assert macro.
 *
 * Sometimes the linker cannot find it so we insert a
 * copy of it in here.
 */

#include <stdio.h>
#include <stdlib.h>

#include <toolsa/os_config.h>

#if !defined(CYGWIN)

void
 __eprintf (const char *string,
	    const char *expression,
	    int line,
	    const char *filename)
{
  fprintf (stderr, string, expression, line, filename);
  fflush (stderr);
  abort ();
}
 
#endif

